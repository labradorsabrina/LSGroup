create table Customers (
    Cust_id Varchar2 (10),
    Cust_gender Varchar2 (10),
    Cust_name Varchar2 (40),
    Cust_address Varchar2 (40),
    Region Varchar2 (10),
    Cust_class Varchar2 (10),
    Cust_card_code Varchar2 (10),
    Prime Varchar2 (10),
    constraint Customers_pk primary key (Cust_id)
);

create table Cards (
    Card_id Varchar2 (10),
    Credit Number,
    Card_status Varchar2 (10),
    Act_date Date,
    Exp_date Date,
    constraint Cards_pk primary key (Card_id)
);

create table Games (
    Prod_id Varchar2 (10),
    Prod_name Varchar2 (40),
    Genre Varchar2 (20),
    Publisher Varchar2 (40),
    Dev_studio Varchar2 (40),
    PEGI_rating Varchar2 (10),
    Metacritic_score Number,
    Playtime Varchar2 (30),
    Edition Varchar2 (30),
    Retail_price Number,
    Wholesale_price Number,
    Max_profit_margin Number,
    Possible_discount Number,
    Monthly_sales Number,
    constraint Games_pk primary key (Prod_id)
);

create table Publishers (
    Publisher_name Varchar2(40),
    Publisher_cut Number,
    Publisher_sales Number,
    Publisher_stock Number,
    Publisher_demand Varchar2(10),
    constraint Publishers_pk primary key (Publisher_name)
);

create table Employees (
    Empl_id Varchar2(10),
    Empl_name Varchar2(40),
    Branch_id Varchar2(10),
    Status Varchar2(20),
    Base_sal Number,
    Commission Number,
    constraint Employees_pk primary key (Empl_id)
);

create table Online_orders (
    Ord_id Varchar2(10),
    Ord_date Date,
    Cust_id Varchar2(10),
    Prod_id Varchar2(10),
    Urgency Varchar2(10),
    Warehouse_id Varchar2(10),
    constraint Online_orders_pk primary key (Ord_id)
);

create table Local_sales (
    Sale_id Varchar2(10),
    Cust_id Varchar2(10),
    Sale_date Date,
    Prod_id Varchar2(10),
    Branch_id Varchar2(10),
    Empl_id Varchar2(10),
    constraint Local_sales_pk primary key (Sale_id)
);

create table Branches (
    Branch_id Varchar2(10),
    Region Varchar2(10),
    Manager_id Varchar2(10),
    Manager_salary Number,
    Sales Number,
    Util_cost Number,
    constraint Branches_pk primary key (Branch_id)
);

create table Warehouses (
    Warehouse_id Varchar2(10),
    Region Varchar2(10),
    Emp_num Number,
    Shipping Varchar2(10),
    constraint Warehouses_pk primary key (Warehouse_id)
);


